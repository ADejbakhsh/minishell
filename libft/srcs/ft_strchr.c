#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	a;

	a = c;
	c = 0;
	while (s[c])
	{
		if (s[c] == a)
			return ((char*)&s[c]);
		c++;
	}
	if (s[c] == a)
		return ((char*)&s[c]);
	return (0);
}
